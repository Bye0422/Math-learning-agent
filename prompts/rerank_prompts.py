def build_rerank_prompt(question, candidates_text):
    return f"""
你是一个 RAG 检索结果重排序器。

你的任务是判断每个资料片段是否能回答用户问题，并给出相关性评分。

评分标准：
- 5 分：高度相关，能直接回答问题，应该优先使用
- 4 分：比较相关，包含重要依据
- 3 分：部分相关，可以辅助回答
- 2 分：弱相关，只包含少量背景信息
- 1 分：几乎无关
- 0 分：完全无关

判断时请特别关注：
1. 是否和用户问题中的题号、题型、概念一致
2. 是否包含题干、公式、选项、条件或结论
3. 是否能为最终回答提供直接依据
4. 不要只因为出现相似词就给高分
5. 如果用户问“第几题”，包含对应题号的片段应优先
6. 如果用户问数学公式或计算过程，包含关键公式的片段应优先

请只输出 JSON 数组，不要输出 Markdown，不要输出解释性文字。

JSON 格式如下：
[
  {{
    "index": 1,
    "score": 5,
    "reason": "该片段直接包含用户问题对应的题干和公式"
  }},
  {{
    "index": 2,
    "score": 2,
    "reason": "该片段只包含相近概念，不能直接回答问题"
  }}
]

【用户问题】
{question}

【候选资料片段】
{candidates_text}
"""